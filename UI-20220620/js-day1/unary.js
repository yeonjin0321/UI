//단항 연산자

let isOk = true;
console.log(isOk);//true
console.log(!isOk);//false
console.log(!!isOk);//true

console.log(!1);//false
console.log(!!1);//true

console.log(+false);//0
console.log(+null);//0
console.log(+'');//0
console.log(+'text');//not a number
console.log(+undefined);//not a number

